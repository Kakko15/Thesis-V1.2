create or replace function public.record_upload_job_event()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_type text;
begin
  if tg_op = 'INSERT' then
    v_type := 'reserved';
  elsif new.cancel_requested_at is distinct from old.cancel_requested_at
      and new.cancel_requested_at is not null then
    v_type := 'cancellation_requested';
  elsif new.status is distinct from old.status then
    v_type := 'status_changed';
  elsif new.stage is distinct from old.stage then
    v_type := 'stage_changed';
  elsif new.cleanup_status is distinct from old.cleanup_status then
    v_type := 'cleanup_changed';
  else
    return new;
  end if;

  insert into public.upload_job_events (
    job_id, owner_id, department, event_type, status, stage,
    attempt_count, worker_id, details
  ) values (
    new.id, new.owner_id, new.department, v_type, new.status, new.stage,
    new.attempt_count, new.lease_owner,
    jsonb_strip_nulls(jsonb_build_object(
      'progress', new.progress,
      'cleanup_status', new.cleanup_status,
      'failure_category', new.failure_category,
      'cancel_requested', new.cancel_requested_at is not null
    ))
  );
  return new;
end;
$$;

drop trigger if exists record_upload_job_event on public.upload_jobs;
create trigger record_upload_job_event
after insert or update on public.upload_jobs
for each row execute function public.record_upload_job_event();

-- Existing retained jobs receive an honest current-state snapshot, not fabricated history.
insert into public.upload_job_events (
  job_id, owner_id, department, event_type, status, stage,
  attempt_count, worker_id, details, created_at
)
select j.id, j.owner_id, j.department, 'migration_snapshot', j.status, j.stage,
  j.attempt_count, j.lease_owner,
  jsonb_build_object('cleanup_status', j.cleanup_status), now()
from public.upload_jobs j
where not exists (
  select 1 from public.upload_job_events e where e.job_id = j.id
);

create or replace function public.request_upload_cancellation(
  p_job_id uuid,
  p_requester_id uuid,
  p_is_superadmin boolean default false,
  p_reason text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_job public.upload_jobs%rowtype;
  v_outcome text;
begin
  select * into v_job from public.upload_jobs where id = p_job_id for update;
  if v_job.id is null then
    return jsonb_build_object('outcome', 'not_found');
  end if;
  if not coalesce(p_is_superadmin, false) and v_job.owner_id is distinct from p_requester_id then
    return jsonb_build_object('outcome', 'forbidden');
  end if;
  if v_job.status in ('completed', 'failed', 'cancelled') then
    return jsonb_build_object('outcome', 'already_terminal', 'status', v_job.status);
  end if;

  if v_job.status = 'processing' then
    update public.upload_jobs
    set cancel_requested_at = coalesce(cancel_requested_at, now()),
        cancel_requested_by = coalesce(cancel_requested_by, p_requester_id),
        cancel_reason = left(nullif(trim(p_reason), ''), 200),
        message = 'Cancellation requested. The worker will stop at the next safe checkpoint.',
        updated_at = now()
    where id = p_job_id;
    v_outcome := 'cancellation_requested';
  else
    update public.upload_jobs
    set status = 'cancelled', stage = 'cancelled', progress = 100,
        message = 'Upload cancelled.', error = null,
        cancel_requested_at = coalesce(cancel_requested_at, now()),
        cancel_requested_by = coalesce(cancel_requested_by, p_requester_id),
        cancel_reason = left(nullif(trim(p_reason), ''), 200),
        cancelled_at = now(), completed_at = now(),
        expires_at = now() + interval '30 days',
        cleanup_status = case when source_stored then 'pending' else 'not_required' end,
        next_retry_at = null, lease_owner = null, lease_expires_at = null,
        heartbeat_at = null, updated_at = now()
    where id = p_job_id;
    v_outcome := 'cancelled';
  end if;
  return (
    select jsonb_build_object(
      'outcome', v_outcome,
      'status', status,
      'cancel_requested', cancel_requested_at is not null,
      'cancelled_at', cancelled_at
    ) from public.upload_jobs where id = p_job_id
  );
end;
$$;

create or replace function public.heartbeat_upload_job_control(
  p_job_id uuid,
  p_worker_id text,
  p_lease_seconds integer default 120,
  p_stage text default null,
  p_progress integer default null,
  p_message text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_cancel_requested boolean;
begin
  update public.upload_jobs
  set lease_expires_at = now() + make_interval(secs => p_lease_seconds),
      heartbeat_at = now(), stage = coalesce(p_stage, stage),
      progress = coalesce(p_progress, progress),
      message = case when cancel_requested_at is null then coalesce(p_message, message) else message end,
      updated_at = now()
  where id = p_job_id and status = 'processing'
    and lease_owner = p_worker_id and lease_expires_at >= now()
  returning cancel_requested_at is not null into v_cancel_requested;
  if not found then
    return jsonb_build_object('lease_valid', false, 'cancel_requested', false);
  end if;
  return jsonb_build_object('lease_valid', true, 'cancel_requested', v_cancel_requested);
end;
$$;

