create or replace function public.apply_operations_retention(p_apply boolean default false)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_events bigint;
  v_alerts bigint;
  v_security bigint;
begin
  select count(*) into v_events from public.upload_job_events
    where created_at < now() - interval '90 days';
  select count(*) into v_alerts from public.operational_alerts
    where status = 'resolved' and resolved_at < now() - interval '90 days';
  select count(*) into v_security from public.security_audit_events
    where created_at < now() - interval '1 year';
  if coalesce(p_apply, false) then
    delete from public.upload_job_events where created_at < now() - interval '90 days';
    delete from public.operational_alerts
      where status = 'resolved' and resolved_at < now() - interval '90 days';
    delete from public.security_audit_events where created_at < now() - interval '1 year';
  end if;
  return jsonb_build_object(
    'applied', coalesce(p_apply, false),
    'upload_job_events', v_events,
    'resolved_operational_alerts', v_alerts,
    'security_audit_events', v_security
  );
end;
$$;

-- Cancellation-aware replacements for queue claim, retry, commit, cleanup, and expiry.
create or replace function public.claim_upload_job(p_worker_id text, p_lease_seconds integer default 120)
returns setof public.upload_jobs
language plpgsql security definer set search_path = public
as $$
declare v_job_id uuid;
begin
  if nullif(trim(p_worker_id), '') is null or p_lease_seconds not between 30 and 900 then
    raise exception 'Invalid worker lease request';
  end if;
  update public.upload_jobs
  set status = 'failed', stage = 'error', progress = 100,
      message = 'Private source staging did not complete.', error = 'Please submit the manuscript again.',
      failure_category = 'staging_timeout', source_stored = source_path is not null,
      cleanup_status = case when source_path is not null then 'pending' else 'not_required' end,
      completed_at = now(), expires_at = now() + interval '30 days', updated_at = now()
  where status = 'staging' and updated_at < now() - interval '15 minutes'
    and cancel_requested_at is null;
  update public.upload_jobs
  set status = 'cancelled', stage = 'cancelled', progress = 100,
      message = 'Upload cancelled.', cancelled_at = now(), completed_at = now(),
      expires_at = now() + interval '30 days',
      cleanup_status = case when source_stored then 'pending' else 'not_required' end,
      lease_owner = null, lease_expires_at = null, heartbeat_at = null, updated_at = now()
  where status = 'processing' and lease_expires_at < now() and cancel_requested_at is not null;
  update public.upload_jobs
  set status = 'failed', stage = 'error', progress = 100,
      message = 'Ingestion could not complete after the allowed retries.',
      error = 'Please submit the manuscript again later.',
      failure_category = coalesce(failure_category, 'worker_lease_expired'),
      cleanup_status = case when source_stored then 'pending' else 'not_required' end,
      completed_at = now(), expires_at = now() + interval '30 days',
      lease_owner = null, lease_expires_at = null, updated_at = now()
  where status = 'processing' and lease_expires_at < now()
    and attempt_count >= max_attempts and cancel_requested_at is null;
  select id into v_job_id from public.upload_jobs
  where (status = 'queued'
      or (status = 'retry_wait' and coalesce(next_retry_at, now()) <= now())
      or (status = 'processing' and lease_expires_at < now()))
    and cancel_requested_at is null and attempt_count < max_attempts and source_stored = true
  order by coalesce(next_retry_at, created_at), created_at
  for update skip locked limit 1;
  if v_job_id is null then return; end if;
  return query update public.upload_jobs
  set status = 'processing', stage = 'download', progress = 10,
      message = 'Durable worker claimed the ingestion job.', attempt_count = attempt_count + 1,
      lease_owner = p_worker_id,
      lease_expires_at = now() + make_interval(secs => p_lease_seconds),
      heartbeat_at = now(), started_at = coalesce(started_at, now()), updated_at = now()
  where id = v_job_id returning *;
end;
$$;

create or replace function public.schedule_upload_retry(
  p_job_id uuid, p_worker_id text, p_retry_at timestamptz, p_failure_category text
)
returns boolean language plpgsql security definer set search_path = public
as $$
begin
  update public.upload_jobs set status = 'retry_wait',
      message = 'A temporary service problem occurred. The job will retry automatically.',
      error = null, failure_category = left(p_failure_category, 120),
      next_retry_at = greatest(p_retry_at, now()),
      lease_owner = null, lease_expires_at = null, heartbeat_at = null, updated_at = now()
  where id = p_job_id and status = 'processing' and lease_owner = p_worker_id
    and lease_expires_at >= now() and attempt_count < max_attempts
    and cancel_requested_at is null;
  return found;
end;
$$;

create or replace function public.commit_upload_ingestion(
  p_job_id uuid, p_worker_id text, p_paper jsonb, p_chunks jsonb
)
returns uuid language plpgsql security definer set search_path = public
as $$
declare v_job public.upload_jobs%rowtype; v_paper_id uuid;
begin
  select * into v_job from public.upload_jobs where id = p_job_id for update;
  if v_job.id is null then raise exception 'Upload job not found'; end if;
  if v_job.status = 'completed' and v_job.paper_id is not null then return v_job.paper_id; end if;
  if v_job.cancel_requested_at is not null then raise exception 'Upload cancellation was requested'; end if;
  if v_job.status <> 'processing' or v_job.lease_owner is distinct from p_worker_id
      or v_job.lease_expires_at < now() then raise exception 'Upload worker lease is no longer valid'; end if;
  if nullif(p_paper ->> 'id', '')::uuid is distinct from p_job_id then
    raise exception 'Paper identifier must match upload job';
  end if;
  v_paper_id := public.commit_paper_ingestion(p_paper, p_chunks);
  update public.upload_jobs set status = 'completed', stage = 'done', progress = 100,
      message = 'Thesis indexed successfully.', paper_id = v_paper_id,
      chunks = jsonb_array_length(p_chunks), duplication = p_paper -> 'duplication_scan',
      error = null, failure_category = null, cleanup_status = 'not_required',
      completed_at = now(), expires_at = now() + interval '30 days',
      lease_owner = null, lease_expires_at = null, heartbeat_at = now(), updated_at = now()
  where id = p_job_id;
  return v_paper_id;
end;
$$;

create or replace function public.claim_upload_cleanup(p_worker_id text)
returns setof public.upload_jobs language plpgsql security definer set search_path = public
as $$
declare v_job_id uuid;
begin
  select id into v_job_id from public.upload_jobs
  where status in ('failed', 'cancelled') and cleanup_status = 'pending' and source_stored = true
  order by completed_at nulls first, created_at for update skip locked limit 1;
  if v_job_id is null then return; end if;
  return query update public.upload_jobs
  set cleanup_status = 'processing', lease_owner = p_worker_id,
      lease_expires_at = now() + interval '120 seconds', updated_at = now()
  where id = v_job_id returning *;
end;
$$;

create or replace function public.expire_upload_jobs()
returns integer language plpgsql security definer set search_path = public
as $$
declare v_deleted integer;
begin
  update public.upload_jobs set cleanup_status = 'pending', lease_owner = null,
      lease_expires_at = null, updated_at = now()
  where cleanup_status = 'processing' and lease_expires_at < now();
  delete from public.upload_jobs where expires_at < now() and (
    (status = 'completed' and cleanup_status = 'not_required')
    or (status in ('failed', 'cancelled') and source_stored = false
      and cleanup_status in ('not_required', 'completed'))
  );
  get diagnostics v_deleted = row_count;
  return v_deleted;
end;
$$;

create or replace function public.upsert_operational_alert(
  p_dedupe_key text,
  p_alert_type text,
  p_severity text,
  p_safe_details jsonb default '{}'::jsonb
)
returns setof public.operational_alerts
language sql
security definer
set search_path = public
as $$
  insert into public.operational_alerts (
    dedupe_key, alert_type, severity, status, safe_details,
    occurrence_count, last_seen_at, updated_at, resolved_at
  ) values (
    left(p_dedupe_key, 160), left(p_alert_type, 120), p_severity,
    'open', coalesce(p_safe_details, '{}'::jsonb), 1, now(), now(), null
  )
  on conflict (dedupe_key) do update set
    alert_type = excluded.alert_type,
    severity = excluded.severity,
    status = case
      when public.operational_alerts.status = 'resolved' then 'open'
      else public.operational_alerts.status
    end,
    safe_details = excluded.safe_details,
    occurrence_count = public.operational_alerts.occurrence_count
      + case when public.operational_alerts.status = 'resolved' then 1 else 0 end,
    last_seen_at = now(),
    updated_at = now(),
    acknowledged_at = case
      when public.operational_alerts.status = 'resolved' then null
      else public.operational_alerts.acknowledged_at
    end,
    acknowledged_by = case
      when public.operational_alerts.status = 'resolved' then null
      else public.operational_alerts.acknowledged_by
    end,
    resolved_at = null
  returning *;
$$;

